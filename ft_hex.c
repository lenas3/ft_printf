#include "ft_printf.h"

int ft_hex(int nb)
{
	char 	*set;
	int 	len;

	set = "0123456789abcdef";
	if(nb < 0)
	{
		ft_char('-');
		len++;
		nb = nb * -1;
	}
	if (nb < 16)
	{
		ft_char(set[nb]);
		len++;
	}
	if(nb >= 16)
	{
		ft_hex((nb / 16));	
		ft_hex((nb % 16));
	}
	len++;
	return len;
}
