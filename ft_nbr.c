#include "ft_printf.h"

int	ft_nbr(int nb)
{
	int	i;

	i = 0;
	if (nb == -2147483648)
	{
		ft_char('-');
		write(1, "2147483648", 10);
		return (11);
	}
	if (nb < 0)
	{
		i = 1;
		ft_char('-');
		nb = -nb;
	}
	if (nb > 9)
	{
		i += ft_nbr(nb / 10);
	}
	ft_char('0' + nb % 10);
	i++;
	return (i);
}
